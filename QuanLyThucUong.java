    private void InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertActionPerformed
        // TODO add your handling code here:

        int maThucUong = Integer.parseInt(txtMaThucUong.getText());
        String tenThucUong = txtTenThucUong.getText();
        String loaiThucUong = txtTenLoaiThucUong.getText();
        String donViTinh = txtDonViTinh.getText();
        int soLuong = Integer.parseInt(txtSoLuong.getText());
        double donGia = Double.parseDouble(txtDonGia.getText());

        ThucUong thucuong = new ThucUong(maThucUong, tenThucUong, loaiThucUong, donViTinh, soLuong, donGia);

        ThucUongModify.insert(thucuong);

        showThucUong();
        JOptionPane.showMessageDialog(rootPane, "Lưu dữ liệu thành công!!!");
    }//GEN-LAST:event_InsertActionPerformed

