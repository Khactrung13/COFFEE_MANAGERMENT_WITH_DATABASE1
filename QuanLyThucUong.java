    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tb.getSelectedRow();
        if (selectedIndex >= 0) {
            ThucUong std = List.get(selectedIndex);

            int option = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xoá thức uống này?");
            System.out.println("option : " + option);

            if (option == 0) {
                ThucUongModify.delete(std.getId());
                showThucUong();
            }
        }
        txtMaThucUong.setText("");
        txtTenThucUong.setText("");
        txtTenLoaiThucUong.setText("");
        txtSoLuong.setText("");
        txtDonViTinh.setText("");
        txtDonGia.setText("");
        JOptionPane.showMessageDialog(rootPane, "Xoá thành công!!!");
    }//GEN-LAST:event_DeleteActionPerformed

    private void FindByNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FindByNameActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Nhập tên thức uống cần tìm:");
        if (input != null && input.length() > 0) {
            List = ThucUongModify.findByFullname(input);

            tableModel.setRowCount(0);

            List.forEach((thucuong) -> {
                tableModel.addRow(new Object[]{thucuong.getId(), thucuong.getTenThucUong(),
                    thucuong.getLoaiThucUong(), thucuong.getDonViTinh(), thucuong.getSoLuong(), thucuong.getDonGia()});
            });
        } else {
            showThucUong();
        }
        JOptionPane.showMessageDialog(rootPane, "Tìm kiếm thành công!!!");
    }//GEN-LAST:event_FindByNameActionPerformed

    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:x   

        if (currentPos >= 0) {

            List.get(currentPos).setId(Integer.parseInt(txtMaThucUong.getText()));
            List.get(currentPos).setTenThucUong(txtTenThucUong.getText());
            List.get(currentPos).setLoaiThucUong(txtTenLoaiThucUong.getText());
            List.get(currentPos).setDonViTinh(txtDonViTinh.getText());
            List.get(currentPos).setSoLuong(Integer.parseInt(txtSoLuong.getText()));
            List.get(currentPos).setDonGia(Double.parseDouble(txtDonGia.getText()));

            ThucUongModify.update(List.get(currentPos));
            currentPos = -1;
        } else {
            ThucUong t = new ThucUong(
                    Integer.parseInt(txtMaThucUong.getText()),
                    txtTenThucUong.getText(),
                    txtTenLoaiThucUong.getText(),
                    txtDonViTinh.getText(),
                    Integer.parseInt(txtSoLuong.getText()),
                    Double.parseDouble(txtDonGia.getText())
            );
            ThucUongModify.insert(t);
            List = ThucUongModify.findAll();
        }
        showThucUong();
        JOptionPane.showMessageDialog(rootPane, "Cập nhật dữ liệu thành công!!!");
        txtMaThucUong.setText("");
        txtTenThucUong.setText("");
        txtTenLoaiThucUong.setText("");
        txtSoLuong.setText("");
        txtDonViTinh.setText("");
        txtDonGia.setText("");

    }//GEN-LAST:event_UpdateActionPerformed

